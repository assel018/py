# Built-in Data Types
# 1. Text Type
x = "Hello"          # str

# 2. Numeric Types
x = 5       # int
y = 3.14    # float
z = 2 + 3j  # complex

# 3. Sequence Types
a = "Hello"          # str
b = [1, 2, 3]       # list
c = (1, 2, 3)       # tuple
d = range(5)        # range

# 4. Mapping Type
e = {"name": "John", "age": 30}  # dict

# 5. Set Types
f = {1, 2, 3}      # set
g = frozenset([1, 2, 3])  # frozenset

# 6. Boolean Type
h = True           # bool

# Binary Types
i = b"Hello"       # bytes
j = bytearray(5)   # bytearray
k = memoryview(bytes(5))  # memoryview

# 6. None Type
i = None           # NoneType



